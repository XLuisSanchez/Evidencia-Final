package codigofuente;

public class Transporte {
	public static String placas;
	public static String num_serie;
	public static String color;
	public static int num_ejes;
	public static int modelo;
	public static String marca;

	public String getPlacas() {
		return placas;
	}

	public void setPlacas(String placas) {
		Transporte.placas = placas;
	}

	public static String getNum_serie() {
		return num_serie;
	}

	public void setNum_serie(String num_serie) {
		Transporte.num_serie = num_serie;
	}

	public static String getColor() {
		return color;
	}

	public void setColor(String color) {
		Transporte.color = color;
	}

	public static int getNum_ejes() {
		return num_ejes;
	}

	public void setNum_ejes(int num_ejes) {
		Transporte.num_ejes = num_ejes;
	}

	public static int getModelo() {
		return modelo;
	}

	public void setModelo(int modelo) {
		Transporte.modelo = modelo;
	}

	public static String getMarca() {
		return marca;
	}

	public void setMarca(String marca) {
		Transporte.marca = marca;
	}

}
